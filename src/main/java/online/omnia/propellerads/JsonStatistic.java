public class JsonStatistic {
    private String show;
    private String click;
    private String convers;
    private String convrate;
    private String cpm;
    private String ctr;
    private String profit;

    public String getShow() {
        return show;
    }

    public void setShow(String show) {
        this.show = show;
    }

    public String getClick() {
        return click;
    }

    public void setClick(String click) {
        this.click = click;
    }

    public String getConvers() {
        return convers;
    }

    public void setConvers(String convers) {
        this.convers = convers;
    }

    public String getConvrate() {
        return convrate;
    }

    public void setConvrate(String convrate) {
        this.convrate = convrate;
    }

    public String getCpm() {
        return cpm;
    }

    public void setCpm(String cpm) {
        this.cpm = cpm;
    }

    public String getCtr() {
        return ctr;
    }

    public void setCtr(String ctr) {
        this.ctr = ctr;
    }

    public String getProfit() {
        return profit;
    }

    public void setProfit(String profit) {
        this.profit = profit;
    }
}
